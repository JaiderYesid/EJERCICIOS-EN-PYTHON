#Programa que permita calcular el 30%, el 60% y el 90% de un número cualquiera

numero = float(input("Ingrese el número: "))

treinta_porciento = numero * 0.30
sesenta_porciento = numero * 0.60
noventa_porciento = numero * 0.90

print(f"El 30% de {numero} es: {treinta_porciento}")
print(f"El 60% de {numero} es: {sesenta_porciento}")
print(f"El 90% de {numero} es: {noventa_porciento}")
